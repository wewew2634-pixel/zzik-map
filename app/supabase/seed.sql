-- ===========================================
-- Generate demo vibe embeddings (128 dimensions)
-- In production, these come from Gemini Vision
-- ===========================================
UPDATE locations SET vibe_embedding = (
  SELECT array_agg(CASE
    WHEN category = 'landmark' THEN 0.7 + random() * 0.3
    WHEN category = 'cafe' THEN 0.5 + random() * 0.5
    WHEN category = 'nature' THEN 0.6 + random() * 0.4
    ELSE 0.4 + random() * 0.4
  END)::vector(128)
  FROM generate_series(1, 128)
);
